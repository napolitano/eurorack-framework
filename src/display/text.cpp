TextMetrics measureText(const std::string_view text, const BitmapFont& font) noexcept {
    if (text.empty()) {
        return {0, font.glyphHeight};
    }

    const std::size_t characterWidth = static_cast<std::size_t>(font.glyphWidth) +
                                       static_cast<std::size_t>(font.horizontalSpacing);

    const std::size_t width =
        text.size() * characterWidth - static_cast<std::size_t>(font.horizontalSpacing);

    return {static_cast<std::int32_t>(std::min<std::size_t>(width, 2147483647U)),
            static_cast<std::int32_t>(font.glyphHeight)};
}

void drawCharacter(MonochromeCanvas& canvas,
                   const Point origin,
                   const char character,
                   const BitmapFont& font,
                   const PixelOperation operation) noexcept {
    if (font.glyphData == nullptr || font.glyphWidth == 0U || font.glyphHeight == 0U) {
        return;
    }

    const auto code = static_cast<std::uint8_t>(character);
    const std::uint8_t safeCode = code >= font.firstCharacter && code <= font.lastCharacter
                                      ? code
                                      : static_cast<std::uint8_t>('?');

    const std::size_t glyphIndex = static_cast<std::size_t>(safeCode - font.firstCharacter);
    const std::size_t offset = glyphIndex * static_cast<std::size_t>(font.glyphWidth);

    for (std::uint8_t column = 0U; column < font.glyphWidth; ++column) {
        const std::uint8_t bits = font.glyphData[offset + column];

        for (std::uint8_t row = 0U; row < font.glyphHeight; ++row) {
            if ((bits & static_cast<std::uint8_t>(1U << row)) != 0U) {
                canvas.applyPixel({origin.x + static_cast<std::int32_t>(column),
                                   origin.y + static_cast<std::int32_t>(row)},
                                  operation);
            }
        }
    }
}

void drawText(MonochromeCanvas& canvas,
              Point origin,
              const std::string_view text,
              const BitmapFont& font,
              const PixelOperation operation) noexcept {
    const std::int32_t advance =
        static_cast<std::int32_t>(font.glyphWidth + font.horizontalSpacing);

    for (const char character : text) {
        drawCharacter(canvas, origin, character, font, operation);
        origin.x += advance;
    }
}

void drawText(MonochromeCanvas& canvas,
              const Rect bounds,
              const std::string_view text,
              const TextStyle& style,
              const BitmapFont& font) noexcept {
    const TextMetrics metrics = measureText(text, font);
    const Point origin = alignedOrigin(bounds, metrics, style);
    const Rect previousClip = canvas.clipRect();

    if (style.clipToBounds) {
        canvas.setClipRect(bounds);
    }

    if (style.mode == TextMode::Inverted) {
        fillRectangle(canvas, bounds, style.foreground);
        drawText(canvas, origin, text, font, style.background);
    } else {
        drawText(canvas, origin, text, font, style.foreground);
    }

    if (style.clipToBounds) {
        canvas.setClipRect(previousClip);
    }
}

