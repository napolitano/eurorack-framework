/*
 * Purpose: Calculates the upper-left origin for aligned text.
 * bounds: Rectangle in which text is aligned.
 * metrics: Measured pixel dimensions of the text.
 * style: Horizontal and vertical alignment policy.
 * Returns: Pixel origin that places the measured text according to `style`.
 */
Point alignedOrigin(const Rect bounds, const TextMetrics metrics, const TextStyle& style) noexcept {
    std::int32_t x = bounds.x;
    std::int32_t y = bounds.y;

    if (style.horizontalAlignment == HorizontalAlignment::Center) {
        x = bounds.x + (bounds.width - metrics.width) / 2;
    } else if (style.horizontalAlignment == HorizontalAlignment::Right) {
        x = bounds.x + bounds.width - metrics.width;
    }

    if (style.verticalAlignment == VerticalAlignment::Middle) {
        y = bounds.y + (bounds.height - metrics.height) / 2;
    } else if (style.verticalAlignment == VerticalAlignment::Bottom) {
        y = bounds.y + bounds.height - metrics.height;
    }

    return {x, y};
}

